# 두개의 봉우리를 가지는 분포가 만들어질 거다. 왜? 성별로 키의 분포가 다르니까
# 그렇다면 이런 분포에서 자료를 뽑아 평균을 계산해도 그 평균은 정규분포를 따를까?
# 만약 중심 극한 정리가 성립된다면 가능하다.

n_sim <- 1000 # 시뮬레이션 횟수
n <- 30 # 한번 추출할때 30명을 추출할 값
means = vector(length = n_sim)
for (i in 1:n_sim){
    y <- vector(length = n) # 길이가 n인 저정소를 만든다....
    for (j in 1:30) { # 30명씩 추출
        gender <- rbinom(1,1,0.5) # 개인별로 성별을 추출
        if (gender == 0){ # 여성
            y[j] <- rnorm(1,160,5) # 여성 집단에서 키를 추출
        }else {
            y[j] <- rnorm(1,175,5) # 남성 집단에서 키를 추출
        } 
    }
    means[i] <- mean(y)  
}
hist(means,xlab="mean_height",ylab="prob",main="Distribution of means",prob=T) 
