# 3. 봉우리를 중심으로 대칭 구조를 가지고 있다.
# 4. 봉우리를 중심으로 대칭되는 지점의 비율이 가까운 곳이 높다는 것을 알수 있다. (중심에 가까울수록 넓이가 넓어서 커진다 == 확률이 높다)
# 5. 1인경우 약 70%의 표준편차(standard deviation), 2인 경우 약 95%정도가 나온다.
