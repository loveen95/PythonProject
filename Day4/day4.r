#    이 정규 분포를 좋은 툴이라고 생각하고 있다. 좋다는 말을 근사적으로 나타내는데 쓸만하다는 의미를 가진다.
#    실제와 다를수 있지만, 크게 차이가 나지 않는다면 현실을 단순화하여 이해할수 있다는 의미이다.
#    저명한 통계박사 조지 박스(George E.P.Box) 가 확률분포의 융요함에 대해서 표현한 글
