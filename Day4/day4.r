## 조건부 확률
# 조건부 확률은 어떤 사건이 참일때 특정 사건의 확률이 얼마인지 일컫는 개념
# 수학적 확률에서 관심있는 경우의 수를 가능한 모든 경우의 수로 나눠서 구할수 있었음.
# 조건은 모든 경우의 발생 가능성이 같다는 것
# 조건부 확률은 수학적 확률에서 분모를 줄이는 방법을 이야기 하는것이다.
#
# 예로 주사위는 던졌을때에 어떤 눈이든 나올 확룔은 수학적으로 1/6인 것을 알고 있다.
# 여기서 주사위를 던졌는데 홀수가 나온 사실을 알았을때, 그 값이 5일 확률은 어떻게 될까?
# 이 경우에 2,4,6은 제외할수 있음. 즉, 가능한 모든 수는 1,3,5이고, 이중 5가 나올 확률은 1/3이 된다.
# P(5|홀수) = 1/3, P(사건|조건)
# P(5|홀수) = P(5,홀수)/P(홀수) =(1/6)/(1/2) = 1/3
# 결국, 조건부 확률은 사건 모두가 일어날 확률을 조건만 일어날 확률로 나눈 것입니다.


## "베이즈정리(Bayes' theorem)"
## 앞 조건부 확률의 공식을 추상적으로 표현하면,
# P(A|B) = P(A,B)/P(B)
# 양변에 P(B)로 곱하면, P(A,B) = P(A|B) x P(B)
# 여기서 A,B는 사건을 말하는 것이기 때문에 
# P(B,A) = P(B|A) x P(A)로 표현할수 있다.
# 이 말은 "A와B가 모두 참이다"와 "B와 A가 모두 참이다" 이것과 같은 의미로 A와B의 순서는 상관이 없다.
# P(B,A) = P(A|B) x P(B)/P(A) *베이즈 정리 공식*
## *베이즈 통계학...확률은 "믿음의 정도"*

#### 베이즈 정리 시뮬레이션하기
# 코로나19 검사
# 모든 검사는 두종류의 오류 가능성이 존재한다.
# "1종 오류(Type 1 Error)" - false positive
# "2종 오류(Type 2 Error)"- false negative
# 1종 오류 - 위양성(false positive) -> 병이 없는데도 양성 결과가 나오는 경우....
# 2종 오류 - 위음성(false negative) -> 병이 있는데도 음성 결과가 나오는 경우....

## 의학에서 양성을 올바르게 진단할 확률을 "민감도"(sensitive)"
# 음성을 올바르게 진단할 확률을 "특이도(specificity)"라고 한다.
# 민감도는 2종 오류를 저지르지 않을 확률을 1에서 2종 오류 확률을 뺀것이다.(여사건)
# 특이도는 1에서 1종 오류의 확률을 뺀 것이다.(여사건)

## 여기서 생각할것은 문제 1,2종 오류의 확률이 둘다 충분히 낮다고 하더라도 상황에 따라 직관에 반하는 결과가 나올수 있다.
# 민감도와 특이도가 모두 99%인 검사가 있다고 가정합니다. 이렇게 높은 경우에도 검사 결과 양성으로 판정된 사람들중 약 10%미만이
# 진양성(true positive)일수 있다. 나머지 90%는 질병이 없는데도 양성으로 잘못된 진단될수 있다. 왜 이런 일이 생길까요 ?
# 이유는 병을 가지고 있지 않은 사람들이 압도적으로 많은 경우에 이런 일들이 생길수 있다.
# 만명 중에 10명이 병이 걸리고, 9990명이 건강하다고 가정합니다. 
# 그럼 0.1%만 병이 있는 것이다. 여기서 구해주는 확률을 0.1%를 "유병률"이라고 한다.
# 그런데 병에 걸렸든 안걸렸든 검사의 정확성은 99%이기 때문에 병에 걸린 10명 전부, 병이 없는 9990중 9890명이 정확히 병이 없는 것으로 진단됨
# 즉 실제로 진단된 사람은 병이 있는 10명과 100이 진단되고, 이것을 비율로 전환하면 10/110 = 1/11 이기 때문에 실제로 병이 있는 비율은 10%가 못됨

## 시뮬레이션 횟수 및 유병률
## 시뮬레이션 횟수 및 유병률
n_sim <- 10000
prevalence <- 0.001

## 검사의 민감도, 특이도
sensitivity <- 0.99
specificity <- 0.99

## 전체 질환 케이스 수, 실제 환자 수, 오진 케이스 수
no_total_positive <- 0
no_true_positive <- 0
no_false_positive <- 0


for (i in 1:n_sim) {
    disease = rbinom(1,1,prevalence)
    if(disease == 0) {
        diagnosis = rbinom(1,1,1-specificity)
        if(diagnosis == 1){
            n_total_positive <- n_total_positive
            n_false_positive <- n_false_positive + 1
        }
    }
    if(disease == 1) {
        diagnosis = rbinom(1,1,sensitivity)
        if(diagnosis == 1) {
            n_total_positive <- n_total_positive + 1
            n_true_positive <- n_true_positive + 1
        }
    }
}

# 양성, 위양성, 진양성 수
print(n_total_positive)
print(n_false_positive)
print(n_true_positive)

# 위양성률
print(n_false_positive/n_total_positive)
