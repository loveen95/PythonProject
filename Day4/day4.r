# 왜? 가능한 모든 경우를 고려했기 때문입니다. 이런 것을 "전사건의 확률"이라고 한다.

# 수학적 확률
# 0, 하나도 맞히지 못하는 경우 :
# 45에서 6개를 제외한 39개중 6개를 선택
# nCr(n=39,r=6) 순서를 고려하지 않기 때문에 조합으로 구한다.
# 39C/45C6 으로 수학적 확률을 구할수 있다.

# 1. 하나만 맞는 경우 :
# 정답 6개 중에 하나 고르고 나머지 39개중 5개를 고르면 됩니다.
# nCr(n=6,r=1) * nCr(n=39,r=5) / nCr(n=45,r=6) 으로 수학적 확률을 구할수 있음
# 2. 두개만 맞는 경우 :
# 정답 6개 중에 두개 고르고 나머지 39개중 4개를 고르면 됨
# nCr(n=6,r=2) * nCr(n=39,r=4) / nCr(n=45,r=6) 

# 0개 맞히는 경우의 수 : nCr(n=6,r=0) * nCr(n=39,r=6)
# 1개 맞히는 경우의 수 : nCr(n=6,r=1) * nCr(n=39,r=5)
# 2개 맞히는 경우의 수 : nCr(n=6,r=2) * nCr(n=39,r=4)
# 3개 맞히는 경우의 수 : nCr(n=6,r=3) * nCr(n=39,r=3)
# 4개 맞히는 경우의 수 : nCr(n=6,r=4) * nCr(n=39,r=2)
# 5개 맞히는 경우의 수 : nCr(n=6,r=5) * nCr(n=39,r=1)
# 6개 맞히는 경우의 수 : nCr(n=6,r=6) * nCr(n=39,r=0)
#### 확률은 위의 경우의 수들을 전사건의 경우의 수인  nCr(n=45,r=6) 로 나누면 된다.

# 위의 경우의 수를 정리 : x를 맞히는 경우의 수(0~6)
## nCr = nPr/r! => n!/(n-r)!/r! 
# nCr(n = 6,r = x) x nCr(n = 39,r = x)

### R로 위에 공식을 구현하시오.
## 조합 계산을 위해서 함수를 사용합니다. choose() 
# choose(n,r) -> n은 전체 개수, r은 추첨하는 총 개수를 의미 
lotto <- function(x) choose(39,6-x) * choose(6,x)

for (x in 0:6) print(lotto(x)) 

# 경의 수에 대한 확률
lotto2 <- function(x) choose(39,6-x) * choose(6,x) / choose(45,6) 
for (x in 0:6) print(lotto2(x))

