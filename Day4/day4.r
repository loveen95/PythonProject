# - 정규 분포 특징
# 1. 확률분포 곡선의 높이는 그 부근 값이 나올 확률과 관련된 값으로, 곡선이 높은 곳 주변의 값들이 낮은 곳 주변의 값들보다 확률이 더 높다.
